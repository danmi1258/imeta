package com.plywet.imeta.core.exception;


public class ImetaFileException extends ImetaException {

	private static final long serialVersionUID = -1513791894680527974L;

	public ImetaFileException() {
		super();
	}

	public ImetaFileException(String message) {
		super(message);
	}

	public ImetaFileException(Throwable cause) {
		super(cause);
	}

	public ImetaFileException(String message, Throwable cause) {
		super(message, cause);
	}

}
